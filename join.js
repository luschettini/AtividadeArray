//Método join.js

//ex. 01
//let palavras (array);
let palavras = ['Sejam', 'Bem', 'Vindos'];

//Usei o console para exibir a let palavras, com o join, ele transforma uma array em string. Deixei a separação (,)
console.log(palavras.join(" "));
//Saída esperada: Sejam Bem Vindos
//ex. 02

//let frutas  (array)
let frutas = ['maça', 'amora', 'morango', 'manga'];

//Eu utilizei como separador a vírgula (,), para unir esses elementos em uma única string.
console.log(frutas.join());
//Saída esperada: maça,amora,morango,manga.

//O método join é uma função que permite unir os elementos de um array em uma única string. 
//Método reverse 

//ex. 01
//let paises (array)
let paises = ['Chicago', 'Dubai', 'Canada', 'França'];

//O reverse é usado para inverter a ordem dos elementos no array.
paises.reverse();
console.log(paises);
//Saída esperada:[ 'França', 'Canada', 'Dubai', 'Chicago' ]


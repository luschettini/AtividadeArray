//Método sort.js

//ex. 01

//let frutas (array)
let frutas = ['cereja', 'mirtilo', 'lichia'];

//O sort é usado para ordenar os elementos do array em ordem alfabética.
frutas.sort();
console.log(frutas);

//Saída esperada:[ 'cereja', 'lichia', 'mirtilo' ].


// Método .Pop

//ex. 01
//vai criar uma variável de convidados.
let convidados = ['Julia', 'Isadora', 'Ana', 'Felipe'];
console.log(convidados);

// vai exibir o array com o último elemento removido
let ultimoConvidado = convidados.pop();
console.log(ultimoConvidado);
console.log(convidados);

//Saída esperada:['Julia', 'Isadora', 'Ana'].

